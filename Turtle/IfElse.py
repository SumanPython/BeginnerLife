import turtle
suman_turtle = turtle.Turtle()
def turtle():
    suman_turtle.forward(100)
    suman_turtle.right(90)
    suman_turtle.forward(100)
    suman_turtle.right(90)
    suman_turtle.forward(100)
    suman_turtle.right(90)
    suman_turtle.forward(100)


elephant_weight = 3000
ant_weight = 0.1
if elephant_weight > ant_weight:
    turtle()